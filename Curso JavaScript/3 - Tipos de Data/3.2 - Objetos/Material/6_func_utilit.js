/* Ferramentas nativas para manipulação e inspeção de objetos:

Object.keys(obj) - Retorna as chaves do objeto

Object.values(obj) - Retorna os valores

Object.entries(obj) - Retorna pares [chave, valor]

Object.assign(dest, src) - Copia propriedades de um objeto para outro

Object.create(proto) - Cria novo objeto com protótipo especificado

Object.freeze(obj) - Congela o objeto (imutável)

Object.seal(obj) - Impede adição/remoção de propriedades

Object.getPrototypeOf(obj) - Obtém o protótipo

Object.setPrototypeOf(obj, proto) - Define um novo protótipo 
*/